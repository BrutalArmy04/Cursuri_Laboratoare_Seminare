FROM EMPLOYEES E3
WHERE E3.DEPARTMENT_ID IN (
    SELECT
        DISTINCT E2.DEPARTMENT_ID
    FROM EMPLOYEES E2
    WHERE E2.SALARY = (
        SELECT
            MAX(E.SALARY)
        FROM EMPLOYEES E
        WHERE E.DEPARTMENT_ID = 30
        )
    );

SELECT *
FROM EMPLOYEES E
WHERE EXISTS(
        SELECT *
        FROM EMPLOYEES E2
        WHERE E2.SALARY = (
            SELECT
                MAX(E4.SALARY)
            FROM EMPLOYEES E4
            WHERE E4.DEPARTMENT_ID = 30) AND
              E2.DEPARTMENT_ID = E.DEPARTMENT_ID
          );

