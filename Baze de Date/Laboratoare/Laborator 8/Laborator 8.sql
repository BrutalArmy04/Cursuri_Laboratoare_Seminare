WITH TABLE_SECUND AS (
    SELECT D.DEPARTMENT_NAME, SUM(E.SALARY) "SUMA TOTALA"
    FROM EMPLOYEES E
    JOIN DEPARTMENTS D ON E.DEPARTMENT_ID = D.DEPARTMENT_ID
    GROUP BY D.DEPARTMENT_NAME
)
SELECT *
FROM TABLE_SECUND T
WHERE T."SUMA TOTALA" > (
        SELECT AVG(T2."SUMA TOTALA")
        FROM TABLE_SECUND T2
    );

