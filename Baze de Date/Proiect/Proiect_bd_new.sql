SELECT 
    J.NUME_UTILIZATOR AS NUME,
    NVL((
        SELECT SUM(R.CANTITATE) 
        FROM RES_JOC_JOC RJJ 
        JOIN RESURSA R ON RJJ.ID_RES = R.ID_RESURSA
        WHERE RJJ.NUME_UTILIZATOR = J.NUME_UTILIZATOR
    ), 0) AS TOTAL_RESURSE,
    C.DATA_INREGISTRARE,
    SUBSTR(J.NUME_UTILIZATOR, 1, 3) || '***' AS NUME_ASCUNS,
    CASE 
        WHEN NVL((
            SELECT SUM(R.CANTITATE) 
            FROM RES_JOC_JOC RJJ 
            JOIN RESURSA R ON RJJ.ID_RES = R.ID_RESURSA
            WHERE RJJ.NUME_UTILIZATOR = J.NUME_UTILIZATOR
        ), 0) > 13 THEN 'BOGAT'
        ELSE 'NORMAL'
    END AS STATUT_FINANCIAR
FROM 
    JUCATOR J
JOIN 
    CONT C ON J.ID_CONT = C.ID_CONT
WHERE 
    EXISTS (
        SELECT 1 
        FROM ORAS O
        JOIN CLADIRE CL ON O.ID_ORAS = CL.ID_ORAS
        WHERE O.NUME_UTILIZATOR = J.NUME_UTILIZATOR
        AND CL.POATE_FI_AFECTATA = 0
    )
ORDER BY 
    TOTAL_RESURSE DESC;


SELECT 
    C.NUME_CARACTER,
    A.NUME_ABILITATE,
    COUNT(SC.SEL_CAR) AS NR_UTILIZARI,
    SUBSTR(A.DESCRIERE, 1, 30) || '...' AS DESCRIERE_SCURTATA,
    TO_CHAR((
        SELECT MAX(DATA_INREGISTRARE) 
        FROM CONT 
        WHERE ID_CONT IN (
            SELECT ID_CONT 
            FROM JUCATOR 
            WHERE NUME_UTILIZATOR IN (
                SELECT NUME_UTILIZATOR 
                FROM SELECTIE_CARACTER
            )
        )
    ), 'MM-YYYY-DD') AS ULTIMA_UTILIZARE,
    RANK() OVER (ORDER BY COUNT(SC.SEL_CAR) DESC) AS CLASAMENT_POPULARITATE,
    CASE 
        WHEN COUNT(SC.SEL_CAR) > 5 THEN 'Popular'
        WHEN COUNT(SC.SEL_CAR) > 2 THEN 'Mediu'
        ELSE 'Puțin utilizat'
    END AS POPULARITATE
FROM CARACTER C
JOIN ABILITATE A ON C.ID_ABILITATE = A.ID_ABILITATE
LEFT JOIN SELECTIE_CARACTER SC ON C.NUME_CARACTER = SC.NUME_CARACTER
WHERE C.NUME_CARACTER IN (
    SELECT NUME_CARACTER 
    FROM (
        SELECT NUME_CARACTER, COUNT(*) AS CNT 
        FROM SELECTIE_CARACTER 
        GROUP BY NUME_CARACTER
        HAVING COUNT(*) > (
            SELECT AVG(COUNT(*)) 
            FROM SELECTIE_CARACTER 
            GROUP BY NUME_CARACTER
        )
    )
)
GROUP BY C.NUME_CARACTER, A.NUME_ABILITATE, A.DESCRIERE
HAVING COUNT(SC.SEL_CAR) > 0
ORDER BY NR_UTILIZARI DESC;

