SELECT 
    J.NUME_UTILIZATOR,
    JC.ID_JOC,
    JC.STATUS,
    NVL(R.TIP, 0) AS TIP_RESURSA,
    NVL(R.CANTITATE, 0) AS CANTITATE_RESURSE,
    NVL(C.NUME, 'Nicio cladire') AS NUME_CLADIRE
FROM JUCATOR J
LEFT JOIN JOACA JO ON J.NUME_UTILIZATOR = JO.NUME_UTILIZATOR
LEFT JOIN JOC JC ON JO.ID_JOC = JC.ID_JOC
LEFT JOIN RESURSA R ON J.NUME_UTILIZATOR = R.NUME_UTILIZATOR
LEFT JOIN (
    SELECT CL.ID_ORAS, CA.NUME 
    FROM CLADIRE CL 
    JOIN CARTE CA ON CL.ID_CARTE = CA.ID_CARTE
) C ON J.NUME_UTILIZATOR = (
    SELECT NUME_UTILIZATOR 
    FROM ORAS 
    WHERE ID_ORAS = C.ID_ORAS
)
ORDER BY J.NUME_UTILIZATOR, JC.ID_JOC;

