CREATE TABLE CONT (
    id_cont NUMBER(4) CONSTRAINT pk_cont PRIMARY KEY,
    email VARCHAR2(50) UNIQUE NOT NULL,
    parola VARCHAR2(30) NOT NULL,
    data_inregistrare DATE NOT NULL
);

CREATE TABLE JUCATOR (
    nume_utilizator VARCHAR2(20) CONSTRAINT pk_jucator PRIMARY KEY,
    id_cont NUMBER(4) CONSTRAINT fk_jucator REFERENCES CONT(id_cont)
);

CREATE TABLE JOC (
    id_joc NUMBER(4) CONSTRAINT pk_joc PRIMARY KEY,
    status VARCHAR2(15) NOT NULL CHECK (status IN ('IN DESFASURARE', 'FINALIZAT'))
);

CREATE TABLE JOACA (
    joc_juc NUMBER(4) CONSTRAINT pk_joaca PRIMARY KEY,
    nume_utilizator VARCHAR2(20) CONSTRAINT fk_joaca_jucator REFERENCES JUCATOR(nume_utilizator),
    id_joc NUMBER(4) CONSTRAINT fk_joaca_joc REFERENCES JOC(id_joc)
);

CREATE TABLE TURA (
    id_tura NUMBER(2) CONSTRAINT pk_tura PRIMARY KEY,
    carac_decart_inviz VARCHAR2(20) NOT NULL
);

CREATE TABLE ABILITATE (
    id_abilitate NUMBER(2) CONSTRAINT pk_abilitate PRIMARY KEY,
    nume_abilitate VARCHAR2(20) NOT NULL,
    descriere VARCHAR2(100) NOT NULL
);

CREATE TABLE CARACTER (
    nume_caracter VARCHAR2(20) CONSTRAINT pk_caracter PRIMARY KEY,
    id_abilitate NUMBER(2) CONSTRAINT fk_caracter_abilitate REFERENCES ABILITATE(id_abilitate),
    numar NUMBER(2) NOT NULL CHECK (numar BETWEEN 1 AND 8)
);

CREATE TABLE SELECTIE_CARACTER (
    sel_car NUMBER(4) CONSTRAINT pk_sel_car PRIMARY KEY,
    nume_utilizator VARCHAR2(20) CONSTRAINT fk_sel_car_jucator REFERENCES JUCATOR(nume_utilizator),
    nume_caracter VARCHAR2(20) CONSTRAINT fk_sel_car_caracter REFERENCES CARACTER(nume_caracter),
    id_tura NUMBER(2) CONSTRAINT fk_sel_car_tura REFERENCES TURA(id_tura),
    caract_decart_viz VARCHAR2(20)
);

CREATE TABLE TEANC (
    id_teanc NUMBER(2) CONSTRAINT pk_teanc PRIMARY KEY,
    id_joc NUMBER(4) CONSTRAINT fk_teanc_joc REFERENCES JOC(id_joc),
    carti NUMBER(2)
);

CREATE TABLE TIP_RESURSA (
    tip NUMBER(1) CONSTRAINT pk_tip_res PRIMARY KEY CHECK(tip in (1,2,3,4,5)),
    nume VARCHAR2(10) NOT NULL
);

CREATE TABLE RESURSA (
    id_resursa NUMBER(5) CONSTRAINT pk_resursa PRIMARY KEY,
    nume_utilizator VARCHAR2(20) CONSTRAINT fk_resursa_jucator REFERENCES JUCATOR(nume_utilizator),
    tip NUMBER(1) CONSTRAINT fk_resursa_tip REFERENCES TIP_RESURSA(tip),
    cantitate NUMBER(2) NOT NULL
);

CREATE TABLE CARTE (
    id_carte NUMBER(2) CONSTRAINT pk_carte PRIMARY KEY,
    tip NUMBER(1) CONSTRAINT fk_carte_tip REFERENCES TIP_RESURSA(tip),
    tip_carte VARCHAR2(15) CHECK (tip_carte IN ('Negustoreasca', 'Boiereasca', 'Religioasa', 'Militara', 'Unica')),
    cost NUMBER(1) NOT NULL CHECK (cost BETWEEN 1 AND 6),
    nume VARCHAR2(20)
);

CREATE TABLE CARTE_UNICA (
    id_carte_unica NUMBER(2) CONSTRAINT pk_carte_unica PRIMARY KEY,
    abilitate NUMBER(2) CONSTRAINT fk_carte_unica_abilitate REFERENCES ABILITATE(id_abilitate),
    CONSTRAINT fk_id_carte_unica FOREIGN KEY (id_carte_unica) REFERENCES CARTE(id_carte)
);

CREATE TABLE CONTINE (
    id_teanc NUMBER(2) CONSTRAINT fk_teanc REFERENCES TEANC(id_teanc),
    id_carte NUMBER(2) CONSTRAINT fk_contine_carte REFERENCES CARTE(id_carte),
    CONSTRAINT pk_contine PRIMARY KEY (id_teanc, id_carte)
);

CREATE TABLE ORAS (
    id_oras NUMBER(1) CONSTRAINT pk_oras PRIMARY KEY,
    nume_utilizator VARCHAR2(20) CONSTRAINT fk_oras_jucator REFERENCES JUCATOR(nume_utilizator)
);

CREATE TABLE CLADIRE (
    id_cladire NUMBER(2) CONSTRAINT pk_cladire PRIMARY KEY,
    id_carte NUMBER(2) CONSTRAINT fk_cladire_carte REFERENCES CARTE(id_carte),
    id_oras NUMBER(1) CONSTRAINT fk_cladire_oras REFERENCES ORAS(id_oras),
    poate_fi_afectata NUMBER(1) CHECK (poate_fi_afectata IN (0, 1))
);



CREATE TABLE FOLOSESTE (
    id_resursa NUMBER(5) CONSTRAINT fk_foloseste_resursa REFERENCES RESURSA(id_resursa),
    id_cladire NUMBER(2) CONSTRAINT fk_foloseste_cladire REFERENCES CLADIRE(id_cladire),
    cantitate_folosita NUMBER(1) NOT NULL CHECK (cantitate_folosita BETWEEN 1 AND 6),
    CONSTRAINT pk_foloseste PRIMARY KEY (id_resursa, id_cladire)
);

