SELECT 
    J.NUME_UTILIZATOR,
    DECODE(J.COROANA, 1, 'Premium', 'Standard') AS TIP_JUCATOR, 
    NVL(R.TOTAL_RESURSE, 0) AS RESURSE, 
    TO_CHAR(C.DATA_INREGISTRARE, 'DD-MM-YYYY') AS DATA_INREGISTRARE, 
    SUBSTR(J.NUME_UTILIZATOR, 1, 3) || '***' AS NUME_ASCUNS, 
    CASE 
        WHEN R.TOTAL_RESURSE > 8 THEN 'Bogat'
        ELSE 'Normal'
    END AS STARE_RESURSE
FROM JUCATOR J
LEFT JOIN (
    SELECT NUME_UTILIZATOR, SUM(CANTITATE) AS TOTAL_RESURSE
    FROM RESURSA
    GROUP BY NUME_UTILIZATOR
) R ON J.NUME_UTILIZATOR = R.NUME_UTILIZATOR
