   NVL(v_categ_modific.id_parinte,0)); 
  
 v_categ2 := v_categ_null; 
  
 DELETE FROM categorii  
 WHERE  id_categorie= v_categ_modific.id_categorie 
 RETURNING id_categorie, denumire, nivel, id_parinte 
