DECLARE
    TYPE varray_tip_plata IS VARRAY(100) OF tip_plata%ROWTYPE;
    TYPE nested_table_tip_plata IS TABLE OF tip_plata%ROWTYPE;
    TYPE index_by_table_tip_plata IS TABLE OF tip_plata%ROWTYPE INDEX BY PLS_INTEGER;

    v_varray varray_tip_plata;
    v_nested_table nested_table_tip_plata;
    v_index_by_table index_by_table_tip_plata;

    CURSOR c_tip_plata IS
        SELECT * FROM tip_plata;

    l_start_time NUMBER;
    l_end_time NUMBER;
    l_total_time NUMBER := 0;
    l_runs NUMBER := 10000;
    l_avg_time NUMBER;
BEGIN
    FOR run_counter IN 1..l_runs LOOP
        l_start_time := DBMS_UTILITY.GET_TIME;

        v_varray := varray_tip_plata();
        FOR rec IN c_tip_plata LOOP
            v_varray.EXTEND;
            v_varray(v_varray.COUNT) := rec;
        END LOOP;

        l_end_time := DBMS_UTILITY.GET_TIME;
        l_total_time := l_total_time + (l_end_time - l_start_time);
    END LOOP;
    l_avg_time := (l_total_time / l_runs) / 100;
    DBMS_OUTPUT.PUT_LINE('VARRAY - Numar teste: ' || l_runs);
    DBMS_OUTPUT.PUT_LINE('VARRAY - Timp mediu de executie: ' || l_avg_time || ' secunde');
end;
