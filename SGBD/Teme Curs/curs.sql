/

CREATE OR REPLACE TRIGGER trg_gestiune_bonusuri
BEFORE INSERT OR UPDATE ON emp_sro
FOR EACH ROW
BEGIN
    IF :NEW.istoric_bonusuri IS NOT NULL THEN
        
        FOR i IN 1 .. :NEW.istoric_bonusuri.COUNT LOOP
            
            IF :NEW.istoric_bonusuri(i).suma <= 0 THEN
                 RAISE_APPLICATION_ERROR(-20001, 'Eroare: Suma bonusului de la pozitia ' || i || ' trebuie sa fie pozitiva!');
            END IF;
            IF :NEW.istoric_bonusuri(i).descriere IS NULL THEN
                :NEW.istoric_bonusuri(i).descriere := 'BONUS STANDARD';
            ELSE
                :NEW.istoric_bonusuri(i).descriere := UPPER(:NEW.istoric_bonusuri(i).descriere);
            END IF;
            IF :NEW.istoric_bonusuri(i).data_acordare IS NULL THEN
                :NEW.istoric_bonusuri(i).data_acordare := SYSDATE;
            END IF;
            
        END LOOP;
    END IF;
END;
